export class SocialMedia {
  website: string;
  
  discord: string;
  
  facebook: string;
  
  twitter: string;
  
  youtube: string;
  
  twitch: string;
  
  constructor(website: string, discord: string, facebook: string, twitter: string, youtube: string, twitch: string) {
    this.website = website;
    this.discord = discord;
    this.facebook = facebook;
    this.twitter = twitter;
    this.youtube = youtube;
    this.twitch = twitch;
  }
}